package mx.gob.stps.portal.movil.app.model.rest;


public class ParametrosBuscadorDTO {

	String tipoConsulta = "0";
	String pagina = "0";
	String resultadoPorPagina = "0";
	String palabraClave = "0";
	String idOferta = "0";
	String idCandidato = "0";
	String idEntidad = "0";
	public String getTipoConsulta() {
		return tipoConsulta;
	}
	public void setTipoConsulta(String tipoConsulta) {
		this.tipoConsulta = tipoConsulta;
	}
	public String getPagina() {
		return pagina;
	}
	public void setPagina(String pagina) {
		this.pagina = pagina;
	}
	public String getResultadoPorPagina() {
		return resultadoPorPagina;
	}
	public void setResultadoPorPagina(String resultadoPorPagina) {
		this.resultadoPorPagina = resultadoPorPagina;
	}
	public String getPalabraClave() {
		return palabraClave;
	}
	public void setPalabraClave(String palabraClave) {
		this.palabraClave = palabraClave;
	}
	public String getIdOferta() {
		return idOferta;
	}
	public void setIdOferta(String idOferta) {
		this.idOferta = idOferta;
	}
	public String getIdCandidato() {
		return idCandidato;
	}
	public void setIdCandidato(String idCandidato) {
		this.idCandidato = idCandidato;
	}
	public String getIdEntidad() {
		return idEntidad;
	}
	public void setIdEntidad(String idEntidad) {
		this.idEntidad = idEntidad;
	}
	
	
}
