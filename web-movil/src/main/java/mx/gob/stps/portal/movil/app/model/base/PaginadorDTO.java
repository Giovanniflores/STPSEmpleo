package mx.gob.stps.portal.movil.app.model.base;

public class PaginadorDTO {

	Boolean detalle;
	
	Integer pagina;
	
	Integer resultadoPorPagina;

	public Boolean getDetalle() {
		return detalle;
	}

	public void setDetalle(Boolean detalle) {
		this.detalle = detalle;
	}

	public Integer getPagina() {
		return pagina;
	}

	public void setPagina(Integer pagina) {
		this.pagina = pagina;
	}

	public Integer getResultadoPorPagina() {
		return resultadoPorPagina;
	}

	public void setResultadoPorPagina(Integer resultadoPorPagina) {
		this.resultadoPorPagina = resultadoPorPagina;
	}
	
	
	
	
}
